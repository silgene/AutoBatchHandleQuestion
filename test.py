import re

def remove_specific_tags(text):
    """
    删除所有 <start>...</end> 标签及其包裹的内容
    功能特性：
    - 支持跨多行内容
    - 自动处理多个连续标签
    - 保留标签外的文本结构
    """
    pattern = r'<start>.*?</end>'  # 非贪婪匹配模式
    return re.sub(pattern, '', text, flags=re.DOTALL)

